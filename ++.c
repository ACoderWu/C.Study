#include <stdio.h>

int main()
{
	int a = 10;
	int b = 10;
	
	/*递增运算只能作用于变量*/
	
	printf("a=%d\n",a);
	printf("b=%d\n",b);
	printf("a++=%d\n",a++);	//++在后面是先算后加 
	printf("++b=%d\n",++b);	//++在前面是先加后算 
	printf("a=%d\n",a);	//执行递增运算时，不管++在变量前面还是后面，最终的输出都要递增1 ，但是被用到时的状态值与++的位置有关 
	printf("b=%d\n",b);
	printf("\n递增运算只能作用于变量，执行递增运算时，不管++在变量前面还是后面，最终的输出都要递增1 ，但是被用到时的状态值与++的位置有关，++在后面是先算后加，++在前面是先加后算\n ");
	
	return 0;
}
